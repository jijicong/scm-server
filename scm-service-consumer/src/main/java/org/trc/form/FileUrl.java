public class FileUrl {

    /**
     * 文件路径
     */
    private String fileKey;
    /**
     * 文件url
     */
    private String url;

    public String getFileKey() {
        return fileKey;
    }

    public void setFileKey(String fileKey) {
        this.fileKey = fileKey;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
